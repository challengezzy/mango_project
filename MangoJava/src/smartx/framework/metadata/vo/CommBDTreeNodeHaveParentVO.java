package smartx.framework.metadata.vo;

import java.io.*;

public class CommBDTreeNodeHaveParentVO implements Serializable {
    String pk;

    String code;

    String name;

    String parentpk;

    public CommBDTreeNodeHaveParentVO(String _pk, String _code, String _name, String _parentpk) {
        this.pk = _pk;
        this.code = _code;
        this.name = _name;
        this.parentpk = _parentpk;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getParentpk() {
        return parentpk;
    }

    public void setParentpk(String parentpk) {
        this.parentpk = parentpk;
    }

    public String getPk() {
        return pk;
    }

    public void setPk(String pk) {
        this.pk = pk;
    }

    public String toString() {
        return name; //+ "(" + code + ")";
    }
}
